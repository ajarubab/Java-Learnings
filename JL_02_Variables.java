public class JL_02_Variables {
    public static void main(String[] args) {

        boolean myBoolean = true;               // 1 bit
        byte myByte = 101;                      // 1 byte
        char myChar = 'A';                      // 2 bytes
        short myShort = 32000;                  // 2 bytes
        int myInt = 1234567890;                 // 4 bytes
        float myFloat = 244.452f;               // 4 bytes
        long myLong = 1234567890123L;           // 8 bytes
        double myDouble = 274651276451.63457D;  // 8 bytes
        String myStr = "Raja";

        System.out.println(myBoolean);
        System.out.println(myByte);
        System.out.println(myChar);
        System.out.println(myShort);
        System.out.println(myInt);
        System.out.println(myFloat);
        System.out.println(myLong);
        System.out.println(myDouble);
        System.out.println(myStr);


    }
}